// Logging a number directly
console.log(3);        // Output: 3 → number

// Logging a string that looks like a number
console.log("3");      // Output: "3" → string

// Performing arithmetic addition
console.log(3 + 4);    // Output: 7 → number (numeric addition)

// Logging a string that looks like an expression
console.log("3+4");    // Output: "3+4" → string (no evaluation, just text)
