// Variable declarations with different data types
a = 5;           // Integer (number)
b = 6.32         // Floating-point number (also treated as number)
c = "Puskar"     // String
d = true;        // Boolean
e = 'k';         // Character (still treated as string in JS)

// Using typeof to check the data type of each variable
console.log(a, typeof (a))   // Output: 5 'number'
console.log(b, typeof b)     // Output: 6.32 'number'
console.log(c, typeof (c))   // Output: "Puskar" 'string'
console.log(d, typeof (d))   // Output: true 'boolean'
console.log(e, typeof (e))   // Output: 'k' 'string'
